# Пример переменных и типов данных в Python

# Целые числа
a = 5
b = -10

# Вещественные числа (числа с плавающей точкой)
pi = 3.14

# Строки (текст)
message = 'Привет, мир!'

# Булевы значения
is_true = True
is_false = False

# Списки (list)
numbers = [1, 2, 3, 4, 5]
names = ['Alice', 'Bob', 'Charlie']

# Кортежи (tuple)
point = (3, 4)

# Словари (dictionary)
person = {'name': 'Alice', 'age': 25, 'is_student': False}

# Нулевое значение (None)
empty_variable = None


# Пример динамической типизации
dynamic_variable = 10  # Это целое число
dynamic_variable = "Hello"  # Теперь это строка
